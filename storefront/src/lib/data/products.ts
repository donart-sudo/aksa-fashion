// Helper to get products for display (converts to card format)
function toCardFormat(p: ScrapedProduct) {
  return {
    id: String(p.id),
    title: p.name,
    handle: p.slug,
    price: p.price * 100, // Convert to cents for formatPrice
    originalPrice: p.salePrice ? p.regularPrice * 100 : undefined,
    thumbnail: p.images[0],
    hoverImage: p.images[1] || undefined,
    badge: p.salePrice
      ? ("sale" as const)
      : p.id > 5200
        ? ("new" as const)
        : undefined,
    colors: p.colors.length > 0 ? p.colors : undefined,
    sizes: p.sizes.length > 0 ? p.sizes : undefined,
  };
}

export function getProductsForCards(limit?: number) {
  const items = limit ? products.slice(0, limit) : products;
  return items.map(toCardFormat);
}

// Filter products by category slug (e.g. "cape-and-train-elegance")
export function getProductsByCategory(categorySlug: string) {
  const categoryName = categories.find((c) => c.handle === categorySlug)?.title;
  if (!categoryName) return [];
  const filtered = products.filter((p) =>
    p.categories.some((c) => c.toLowerCase() === categoryName.toLowerCase())
  );
  return filtered.map(toCardFormat);
}

// Get "new" products (id > 5200 = latest uploads)
export function getNewProducts() {
  return products.filter((p) => p.id > 5200).map(toCardFormat);
}

// Get sale products
export function getSaleProducts() {
  return products.filter((p) => p.salePrice).map(toCardFormat);
}

// Get all in-stock products
export function getInStockProducts(limit?: number) {
  const inStock = products.filter((p) => p.inStock);
  const items = limit ? inStock.slice(0, limit) : inStock;
  return items.map(toCardFormat);
}

// Helper to find a product by slug
export function getProductBySlug(slug: string) {
  return products.find((p) => p.slug === slug) || null;
}
